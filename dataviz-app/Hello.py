import streamlit as st

st.markdown('# Brain-wide representations of spontaneous behavior')

st.markdown('Here, I try to provide some interactive visualizations to explore some of the really interesting data reported in [Stringer & Pachitariu et al. 2019: Spontaneous behaviors drive multidimensional, brainwide activity](https://www.science.org/doi/10.1126/science.aav7893). Utilizing their [open source data](https://janelia.figshare.com/articles/dataset/Recordings_of_ten_thousand_neurons_in_visual_cortex_during_spontaneous_behaviors/6163622), I have replicated their findings that a large fraction of the neural variance in a region of visual cortex represents the mouse\'s spontaneous and uninstructed behavior.')

st.markdown('I highly encourage you to download and explore the data for yourself - there are lots more interesting questions one can ask! You can also take a look at my source code [here](https://github.com/jmmanley/brainwide-behavior-encoding).')


